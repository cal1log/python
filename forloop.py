#!/usr/bin/env python3

listvalues = ['orlando','gomez', 'lopez', 38, 1.64]

''' printing listvalues without for loop '''
print(listvalues)
print()

''' printing listvalues with for loop '''
for i in listvalues:
    print(i)
print()

''' printing listvalues string text with for loop '''
for i in listvalues[0]:
    print(i)
print()

''' printing numbers range 0 to 9 with for loop '''
for i in range(10):
    print(i)
print()

''' printing numbers range 11 to 20 with for loop incrementing by 1 '''
for i in range(11, 21, 1):
    print(i)
print()

''' printing numbers range 11 to 20 with for loop decrementing by 1 '''
for i in range(20, 10, -1):
    print(i)
print()

''' printing numbers range 11 to 20 with for loop incrementing by 1 '''
for i in range(11, 21, 2):
    print(i)
print()

