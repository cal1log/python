#!/usr/bin/env python3

''' number conversion '''

''' decimal to binary '''
print('decimal 25 to binary with bin function bin(25)')
print(bin(25))
print()

''' decimal to octal '''
print('decimal 25 to octal with oct function oct(25)')
print(oct(25))
print()

''' decimal to hexadecimal '''
print('decimal 25 to hexadecimal with hex function hex(25)')
print(hex(25))
print()

''' hexadecimal to decimal '''
print('hexadecimal 0xf to decimal(0xf) equals 15')
print(0xf)
print()

''' octal to decimal '''
print('octal 11 to decimal(0o11) equals 9')
print(0o11)
print()

''' binary to decimal '''
print('binary 11 to decimal(0b11) equals 3')
print(0b11)
print()
